// ---------------------------------------------------------------------------
// Generate hook.yaml files
// ---------------------------------------------------------------------------

const hooksDir = join(ROOT, 'assets', 'prompt-hooks');
mkdirSync(hooksDir, { recursive: true });

let created = 0;
let skipped = 0;

for (const hook of TIER1_HOOKS) {
  const dirName = `${hook.id.toLowerCase()}-${slugify(hook.name)}`;
  const hookDir = join(hooksDir, dirName);

  if (existsSync(join(hookDir, 'hook.yaml'))) {
    skipped++;
    continue;
  }

  mkdirSync(hookDir, { recursive: true });

  const yaml =
    [
      `id: ${hook.id}`,
      `name: ${hook.name}`,
      `stage: ${hook.stage}`,
      `order: ${hook.order}`,
      `version: 1`,
      `enabled: true`,
      '',
      '# Content resolution',
      `template: ${hook.template}`,
      ...(hook.resolver ? [`resolver: ${hook.resolver}`] : []),
      '',
      '# Dependencies',
      `inputs:`,
      ...(hook.inputs.length > 0 ? hook.inputs.map((i) => `  - ${i}`) : ['  []']),
      '',
      '# Override constraints',
      `disableable: ${hook.disableable}`,
      '',
      '# Classification (Phase 1 3-axis)',
      `safetyTier: ${hook.safetyTier}`,
      `transparencyTier: ${hook.transparencyTier}`,
      `governanceTier: ${hook.governanceTier}`,
      '',
      '# CVO-facing',
      ...(hook.userExplanation ? [`userExplanation: "${hook.userExplanation}"`] : []),
    ].join('\n') + '\n';

  writeFileSync(join(hookDir, 'hook.yaml'), yaml, 'utf-8');

  // Create a placeholder template if the Phase 1 template doesn't exist
  const existingTemplate = join(ROOT, 'assets', 'prompt-templates', hook.template);
  const hookTemplate = join(hookDir, hook.template);
  if (existsSync(existingTemplate)) {
    // Symlink to existing Phase 1 template
    // For now, just note the reference — actual symlink in P2-C when pipeline takes over
    writeFileSync(
      join(hookDir, '.template-ref'),
      `# Phase 1 template: assets/prompt-templates/${hook.template}\n`,
      'utf-8',
    );
  } else {
    // Create placeholder
    writeFileSync(
      hookTemplate,
      `<!-- ${hook.id}: ${hook.name} -->\n<!-- Template placeholder —  content in P2-B resolver -->\n`,
      'utf-8',
    );
  }

  created++;
}

